int	ft_sqrt(int x)
{
	long	nb;
	long	i;

	nb = x;
	i = 0;
	if (nb == 0 || nb == 1)
		return (nb);
	while (nb >= (i * i))
	{
		if ((i * i) == nb)
			return (i);
		else
			i++;
	}
	return (0);
}
