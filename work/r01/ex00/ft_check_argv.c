#include"a.h"

int	ft_check_argv(char **argv)
{
	int	y;

	y = 0;
	while (argv[1][y])
	{
		if ((argv[1][y] < '0' && argv[1][y] > '9') || argv[1][y] == ' ')
			return (0);
		y++;
	}
	return (1);
}
