#include"a.h"

int	game_over(char **tab)
{
	int	i;
	int	j;

	i = 1;
	j = 1;
	while (i <= 4)
	{
		while (j <= 4)
		{
			if (tab[i][j] == '\0')
			{
				return (0);
			}
		}
	}
	return (1);
}
