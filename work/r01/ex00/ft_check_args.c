#include"a.h"

int	ft_check_lines(char **tab, int x, int y)
{
	x = 0;
	y = 0;
	while ((tab[0][y] <= '4' && tab[0][y] >= '1') && \
			(tab[5][y] <= '4' && tab[5][y] >= '1'))
	{
		if ((tab[0][y] + tab[5][y]) <= '5' && (tab[0][y] + tab[5][y]) >= '3')
		{
			return (1);
		}
		y++;
	}
	while ((tab[x][0] <= '4' && tab[x][0] >= '1') && \
			(tab[x][5] <= '4' && tab[x][5] >= '1'))
	{
		if ((tab[x][0] + tab[x][5]) <= '5' && (tab[x][0] + tab[x][5]) >= '3')
		{
			return (1);
		}
		x++;
	}
	return (0);
}
